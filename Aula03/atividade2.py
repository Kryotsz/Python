num=int(input('Digite um número: '))
dobro=num*2
triplo=num*3
raiz=num**(1/2)
print('O número que você digitou foi {}, o dobro desse número é {}, o triplo é {} e a raíz quadrada é {}.'.format(num, dobro, triplo, raiz))
# raiz quadrada no python é **(1/2) depois do número
# é melhor sempre que der usar o int ao invés do float, pois o float usa mais memória
